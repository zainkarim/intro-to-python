# Strings
last_letter = "z" # this is a string (literal)
print(last_letter) # "z"

print("Hello")
hello = 5
print(hello) # 5

print(4 + 7) # 11
print("4 + 7") # "4 + 7"

print("hello " + "my name") # string concatenation, not addition

# Conversion
print(float(4)) # 4.0
print(int(3.6)) # 3
print(str(4) + str(2)) # string concatenation, "42"
print(int("4") + int("2")) # addition, 6

# Booleans
x = True # 1
print(x) # True

y = False # 0
print(y) # False

